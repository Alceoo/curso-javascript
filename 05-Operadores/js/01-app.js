/*Los operadores nos ayudan a comparar distintas variables */

const numero1 = 20; 
const numero2 = '20'; 
const numero3 = 30;

// Operador mayor a...

console.log(numero1 > numero3);
//Nos va a arrojar un booleano, en este caso es false, ya que 20 no es mayor a 30
console.log(numero3 > numero1);
//Arrojará un true, ya que numero 3 sí es mayor a numero uno.


// Operador menor a...
console.log(numero1 < numero3);
//Numero 1 sí es menor a numero 3, por lo tanto nos va a arrojar un true
console.log(numero3 < numero1);
//Aquí nos va a arrojar un false, ya que numero 3 es mayor que número uno.
